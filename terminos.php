                <p>3.2. Acceso y Seguridad: El Usuario es responsable de mantener la confidencialidad de sus
                    credenciales de acceso y debe notificar al Proveedor de Servicios de inmediato en caso de
                    acceso no autorizado a su cuenta.</p>
