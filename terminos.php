                <p> 8.1. Soporte Técnico: El Proveedor de Servicios ofrece soporte técnico para resolver problemas
                    relacionados con el funcionamiento del SaaS Softmor POS.</p>
                <p> 8.2. Acceso Autorizado para Soporte: En caso de que un usuario requiera asistencia o soporte
