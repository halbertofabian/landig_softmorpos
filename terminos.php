                    meses desde el inicio de la prueba gratuita, la base de datos asignada al Usuario será
                    eliminada y podrían perder información almacenada en ella.</p>
                <!-- Artículo 5 -->
                <h6>Artículo 5: Inactividad de Usuarios</h6>
                <p>5.1. Eliminación de Base de Datos por Inactividad: Los Usuarios que hayan adquirido una
                    licencia completa y se mantengan inactivos, sin utilizar el SaaS Softmor POS durante un
                    período de 1 año, podrían tener su base de datos asignada eliminada.
                </p>
