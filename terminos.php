                <p>2.1. Concesión de Licencia: El Proveedor de Servicios otorga al Usuario una licencia no exclusiva, no transferible y revocable para acceder y utilizar el SaaS Softmor POS de acuerdo con los términos y condiciones establecidos en este acuerdo.</p>
