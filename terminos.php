                <h6>Artículo 6: Prohibición de Reventa</h6>
                <p>6.1. Prohibición de Reventa: La reventa de licencias vitales y paquetes multi-sucursal del SaaS
                    Softmor POS está prohibida. Los Usuarios que violen esta prohibición tendrán sus cuentas
                    desactivadas de manera inmediata.</p>
